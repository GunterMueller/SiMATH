/*M
.SH NAME
mapnfexp - matrix of polynomials over number field, exponentiation

.SH SYNOPSIS
.nf
#include<_matr3.h>

matrix mapnfexp(r, F, M, n)
single r;
pol F;
matrix M;
single n;

.SH DESCRIPTION
mapnfexp() returns the matrix M^n.
For n<0 the determinant of M must be invertible.


.PP
.TP 4
.B F
is an irreducible monic dense polynomial in one variable over Z.
.TP 4
.B M
must be a square matrix of polynomials in r variables over the number field generated by F.
.TP 4
.B n
with -BASIS+1 <= n <= BASIS-1 is the exponent of the result.
M*/





/*H
	Version 1       06.06.1990      Th. Weis
	Version 2       27.01.1994      Georg Rock
                                        Habe  induktives Potenzieren,d.h.
                                        A^n = A*A*A*A*A... (n-mal) durch
                                        binaeres Potenzieren ersetzt.
   DATE mapnfexp      : 910915.941201
H*/

#include <_matr3.h>

matrix mapnfexp( r, F, M, n )
single r;
pol    F;
matrix M;
single n;

{
	matrix M1;
	single m;

	bind( F, M );
	init( M1 );

/* n gleich 0 */

	if ( !n )
	{
	   m = manrrow( M );
	   M1 = mapnfcons1( r, F, m );
	}

/* M gleich 0 */

	else if ( isnullma( M ) ) M1 = M;

/* n, M ungleich 0 */

	   else
	   {
	      if ( n < 0 )
	      {
	         M = mapnfinv( r, F, M );
	         n = -n;
	      }
              m = manrrow( M );
	      for (M1 = mapnfcons1( r, F, m ); n; n /= 2) /* Binaeres Potenzieren */
                {
                      if (n%2)  M1 = mapnfprod( r, F, M1, M );

                      if (n != 1) M = mapnfprod( r, F, M, M );
                }

	   }

/* Ausgabe */

	return( M1 );
}



