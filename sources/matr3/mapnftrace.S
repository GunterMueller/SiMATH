/*M
.SH NAME
mapnftrace - matrix of polynomials over number fields, trace

.SH SYNOPSIS
.nf
#include<_matr3.h>

pol mapnftrace(r, F, M)
single r;
obj F;
matrix M;

.SH DESCRIPTION
mapnftrace() returns the trace of the matrix M.

.PP
.TP 4
.B F
is an irreducible monic dense polynomial in one variable over Z.
.TP 4
.B M
must be a square matrix of polynomials in r variables over the number field generated by F.
M*/



/*H
	Version 1       11.07.1994      S. Schmitt
   DATE mapnftrace    : 941201
H*/

#include <_matr3.h>

pol mapnftrace(r,F,M)
single r;
obj F;
matrix M;
{
	single i,j;
	list Z1;
	obj S;
	bind(F,M);
	init(S);
         
	S=itopi(r,0);       
	S = pitopnf(r,0);
	i = 0;
	while ( M != _0 ) {
		i++;
		Z1 = lfirst(M);
		M  = lred(M);
		for(j=1;j<i;j++) Z1=lred(Z1);
		S = pnfsum(r,F,S,lfirst(Z1));
	}
	return(S);
}


