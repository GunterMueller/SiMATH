/*M
.SH NAME
vecpnfsmul - vector of polynomials over number field, scalar multiplication

.SH SYNOPSIS
.nf
#include<_matr3.h>

vec vecpnfsmul(r, F, U, el)
single r;
pol F;
vec U;
pol el;

.SH DESCRIPTION
vecpnfsmul() returns the vector el*U.
.PP
.TP 4
.B F
is an irreducible monic dense polynomial in one variable over Z.
.TP 4
.B U
is a vector of polynomials in r variables over the number field generated by F.
.TP 4
.B el
is a polynomial over the number field generated by F.
M*/









/*H
	Version 1       05.06.1990      Th. Weis
   DATE vecpnfsmul    : 910915
H*/

#include <_matr3.h>

vec vecpnfsmul( r, F, U, el )
single r;
pol    F;
vec    U;
pol    el;

{          
	pol    e;
	single n;
	vec    W;

	bind( F, U, el );
	init( e, W );

/* el gleich 0 */

	if( !el )
	{
	   n = llength( U );
	   W = _0;
	   while( n-- ) W = lcomp( 0, W );
	}

/* allgemeiner Fall */

	else
	{
	   W = _0;
	   do
	   {
	      e = lfirst( U );
	      U = lred( U );
	      e = pnfprod( r, F, e, el );
	      W = lcomp( e, W );
	   }
	   while ( U != _0 );
	   W = linv( W );
	}
	return( W );
}


