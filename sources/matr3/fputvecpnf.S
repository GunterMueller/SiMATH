/*M
.SH NAME
fputvecpnf - file put vector of polynomials over number field (MACRO)

.SH SYNOPSIS
.nf
#include<_matr3.h>

single fputvecpnf(r, F, V, VL, VLnf, pf)
single r;
pol F;
vec V;
list VL,VLnf;
FILE *pf;

.SH DESCRIPTION
fputvecpnf() writes the vector V to the output file *pf
and returns 0 or ERROR in case an error has occurred.
.PP
.TP 4
.B F
is an irreducible monic dense polynomial in one variable over Z.
.TP 4
.B V
is a vector of polynomials in r variables over the number field generated by F.
.TP 4
.B VL
is a variable list of length r.
.TP 4
.B VLnf
is a variable list containing the name of a primitive
element of the field extension of Q.
.TP 4
.B pf
is a pointer to the output file.

.SH SEE ALSO
fputnf
M*/









