/*M
.SH NAME
vecpnftovpmp - vector of polynomials over number field to vector of polynomials modulo polynomial over number field

.SH SYNOPSIS
.nf
#include<_matr3.h>

vec vecpnftovpmp(r, F, P, V)
single r;
pol F,P;
vec V;

.SH DESCRIPTION
vecpnftovpmp() returns the vector V with all entries converted
to polynomials in r variables over the number field generated by F.
These polynomials are reduced modulo P.

.PP
.TP 4
.B F
is an irreducible monic dense polynomial in one variable over Z.
.TP 4
.B P
is a polynomial in r variables over the number field generated by F.
M*/








/*H
	Version 1       18.09.1992	U.Schneiders
   DATE vecpnftovpmp  : 921130
H*/

#include <_matr3.h>

vec vecpnftovpmp( r, F, P, V )
single r;
pol F, P;
vec V;
{
	pol el;
	vec H;
	init(el,H);
	bind(F,P,V);

	H = _0;
	while ( V != _0 ) {
	        el = lfirst(V);
		V  = lred(V);
		el = pnfrem(r,F,el,P);
		H  = lcomp(el,H);
	}
	if ( H != _0 ) V = linv(H);
	return(V);
}


