/*M
.SH NAME
fgetmapnf - file get matrix of polynomials over number field (MACRO)

.SH SYNOPSIS
.nf
#include<_matr3.h>

matrix fgetmapnf(r, F, VL, VLnf, pf)
single r;
pol F;
list VL,Vnf;
FILE *pf;

.SH DESCRIPTION
fgetmapnf() returns the matrix of polynomials in r variables over the number field generated by F,
read from the input file *pf or ERROR in case an error has occurred.
.PP
.TP 4
.B F
is an irreducible monic dense polynomial in one variable over Z.
.TP 4
.B VL
is a variable list of length r.
.TP 4
.B VLnf
is a variable list containing the name of a primitive
element of the field extension of Q.
.TP 4
.B pf
is a pointer to the input file.

.SH SEE ALSO
fgetpnf
M*/










