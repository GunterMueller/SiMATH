/*M
.SH NAME
mapnfsmul - matrix of polynomials over number field, scalar multiplication

.SH SYNOPSIS
.nf
#include<_matr3.h>

matrix mapnfsmul(r, F, M, el)
single r;
pol F;
matrix M;
pol el;

.SH DESCRIPTION
mapnfsmul() returns the matrix el*M.

.PP
.TP 4
.B F
is an irreducible monic dense polynomial in one variable over Z.
.TP 4
.B M
is a matrix of polynomials in r variables over the number field generated by F.
.TP 4
.B el
is a polynomial over the number field generated by F.
M*/









/*H
	Version 1       01.06.1990      Th. Weis
   DATE mapnfsmul     : 910915
H*/

#include <_matr3.h>

matrix mapnfsmul( r, F, M, el )
single r;
pol    F;
matrix M;
pol    el;

{
	list   Z, ZM;
	matrix H, N;
	pol    e;

	bind( F, M, el );
	init( H, N, Z, ZM, e );

/* Elementweise Multiplikation */

	H = macopy( M );
	N = _0;
	while( H != _0 )
	{
	   Z = lfirst( H );
	   H = lred( H );
	   ZM = _0;
	   while( Z != _0 )
	   {
	      e = lfirst( Z );
	      Z = lred( Z );
	      e = pnfprod( r, F, e, el );
	      ZM = lcomp( e, ZM );
	   }
	   ZM = linv( ZM );
	   N = lcomp( ZM, N );
	}
	N = linv( N );
	return( N );
}


