/*M
.SH NAME
vpnfsprod - vector of polynomials over number field, scalar product

.SH SYNOPSIS
.nf
#include<_matr3.h>

pol vpnfsprod(r, F, V, W)
single r;
pol F;
vec V,W;

.SH DESCRIPTION
vpnfsprod() returns the scalar product U*V.

.PP
.TP 4
.B F
is an irreducible monic dense polynomial in one variable over Z.
.TP 4
.B U,V
are vectors of polynomials in r variables over the number field generated by F.
U and V must have equal length.
M*/









/*H
	Version 1       05.06.1990      Th. Weis
   DATE vpnfsprod     : 910915
H*/

#include <_matr3.h>

pol vpnfsprod( r, F, V, W )
single r;
pol    F;
vec    V, W;

{
	pol s, v, w;

	bind( F, V, W );
	init( s, v, w );

	s = 0;
	while ( V != _0 )
	{
	   v = lfirst( V ), V = lred( V );
	   w = lfirst( W ), W = lred( W );
	   s = pnfsum( r, F, pnfprod( r, F, v, w ), s );
	}
	return( s );
}


