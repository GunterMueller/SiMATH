/*M
.SH NAME
vecpnflc - vector of polynomials over number field, linear combination

.SH SYNOPSIS
.nf
#include<_matr3.h>

vec vecpnflc(r, F, s1, s2, V1, V2)
single r;
pol F,s1,s2;
vec V1,V2;

.SH DESCRIPTION
vecpnflc() returns the linear combination (s1*V1)+(s2*V2).


.PP
.TP 4
.B F
is an irreducible monic dense polynomial in one variable over Z.
.TP 4
.B s1,s2
are polynomials over the number field generated by F.
.TP 4
.B V1,V2
are vectors of polynomials in r variables over the number field generated by F.
V1 and V2 must have equal length.
M*/






/*H
	Version 1       05.06.1990      Th. Weis
   DATE vecpnflc      : 910915
H*/

#include <_matr3.h>

vec vecpnflc( r, F, s1, s2, L1, L2 )
single r;
pol    F, s1, s2;
vec    L1, L2;

{
	vec Z;
	pol a1, a2;

	bind( F, s1, s2, L1, L2 );
	init( Z, a1, a2 );

	Z = _0;
	while ( L1 != _0 )
	{
	   a1 = pnfprod( r, F, s1, lfirst( L1 ) );
	   a2 = pnfprod( r, F, s2, lfirst( L2 ) );
	   a1 = pnfsum( r, F, a1, a2 );
	   L1 = lred( L1 );
	   L2 = lred( L2 );
	   Z = lcomp( a1, Z );
	}
	Z = linv( Z );
	return( Z );
}


