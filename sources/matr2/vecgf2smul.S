/*M
.SH NAME
vecgf2smul - vector over Galois-field of characteristic 2, scalar multiplication

.SH SYNOPSIS
.nf
#include<_matr2.h>

vec vecgf2smul(G, V, el) 
pol G;
vec V;
gf2el el;

.SH DESCRIPTION
vecgf2smul() returns the vector el*V.
.PP
.TP 4
.B G
is an irreducible monic polynomial in one variable over Z/2Z.
.TP 4
.B V
is a vector of the Galois-field generated by G.
.TP 4
.B el
is an element of the Galois-field generated by G.
M*/


/*H
	Version 1       08.03.1996      S. Schmitt
   DATE vecgf2smul    : 960501
H*/

#include <_matr2.h>

vec vecgf2smul( G, V, el )
pol   G;
vec    V;
gf2el   el;

{          
	gf2el   e;
	single n;
	vec    W;

	bind( G, V, el );
	init( e, W );

/* el gleich 0 */

	if( !el )
	{
	   n = llength( V );
	   W = _0;
	   while( n-- ) W = lcomp( 0, W );
	}

/* allgemeiner Fall */

	else
	{
	   W = _0;
	   do
	   {
	      e = lfirst( V );
	      V = lred( V );
	      e = gf2prod( G, e, el );
	      W = lcomp( e, W );
	   }
	   while ( V != _0 );
	   W = linv( W );
	}
	return( W );
}
