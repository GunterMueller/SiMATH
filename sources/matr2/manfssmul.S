/*M
.SH NAME
manfssmul - matrix of number field elements, sparse representation, scalar multiplication

.SH SYNOPSIS
.nf
#include<_matr2.h>

matrix manfssmul(F, M, el)
pol F;
matrix M;
nfel el;

.SH DESCRIPTION
manfssmul() returns the matrix el*M.

.PP
.TP 4
.B F
is an irreducible sparse polynomial in one variable over the rational numbers.
.TP 4
.B M
is a matrix over the number field generated by F.
.TP 4
.B el
is an element of the number field generated by F.
M*/


/*H
	Version 1       11.04.1990      Th. Weis
   DATE manfssmul     : 910915
H*/

#include <_matr2.h>

matrix manfssmul( F, M, el )
pol    F;
matrix M;
nfel   el;

{
	list   Z, ZM;
	matrix H, N;
	nfel   e;

	bind( F, M, el );
	init( H, N, Z, ZM, e );

	/*** Elementweise Multiplikation. ***/

	H = macopy( M );
	N = _0;
	while( H != _0 )
	{
	   Z = lfirst( H );
	   H = lred( H );
	   ZM = _0;
	   while( Z != _0 )
	   {
	      e = lfirst( Z );
	      Z = lred( Z );
	      e = nfsprod( F, e, el );
	      ZM = lcomp( e, ZM );
	   }
	   ZM = linv( ZM );
	   N = lcomp( ZM, N );
	}
	N = linv( N );
	return( N );
}
