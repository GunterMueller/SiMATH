/*M
.SH NAME
magf2smul - matrix over Galois-field of characteristic 2, scalar multiplication

.SH SYNOPSIS
.nf
#include<_matr2.h>

matrix magf2smul(G, M, el) 
pol G;
matrix M;
gf2el el;

.SH DESCRIPTION
magf2smul() returns the matrix el*M.

.PP
.TP 4
.B G
is an irreducible monic polynomial in one variable over Z/2Z.
.TP 4
.B M
is a matrix over the Galois-field generated by G.
.TP 4
.B el
is an element of the Galois-field generated by G.
M*/


/*H
	Version 1       08.03.1996      S. Schmitt
   DATE magf2smul     : 960501
H*/

#include <_matr2.h>

matrix magf2smul(  G, M, el )
pol    G;
matrix M;
gf2el   el;

{
	gf2el   e;
	list   Z, ZM;
	matrix H, N;

	bind( G, M, el );
	init( H, N, Z, ZM, e );

	/*** Elementweise Multiplikation. ***/

	H = macopy( M );
	N = _0;
	while( H != _0 )
	{
	   Z = lfirst( H );
	   H = lred( H );
	   ZM = _0;
	   while( Z != _0 )
	   {
	      e = lfirst( Z );
	      Z = lred( Z );
	      e = gf2prod( G, e, el );
	      ZM = lcomp( e, ZM );
	   }
	   ZM = linv( ZM );
	   N = lcomp( ZM, N );
	}
	N = linv( N );
	return( N );
}
