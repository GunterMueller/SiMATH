/*M
.SH NAME
fputmanfs - file put matrix of number field elements, sparse representation (MACRO)

.SH SYNOPSIS
.nf
#include<_matr2.h>

single fputmanfs(F, M, VL, pf)
pol F;
matrix M;
list VL;
FILE *pf;

.SH DESCRIPTION
fputmanfs() writes the matrix M to the output file *pf
and returns 0 or ERROR in case an error has occurred.
.PP
.TP 4
.B F
is an irreducible monic sparse polynomial in one variable over the rational numbers.
.TP 4
.B M
is a matrix over the number field generated by F.
.TP 4
.B VL
is a variable list of length 1.
.TP 4
.B pf
is a pointer to the output file.

.SH SEE ALSO
fputnfels
