/*M
.SH NAME
fputvecnfs - file put vector of number field elements, sparse representation (MACRO)

.SH SYNOPSIS
.nf
#include<_matr2.h>

single fputvecnfs(F, V, VL, pf)
pol F;
vec V;
list VL;
FILE *pf;

.SH DESCRIPTION
fputvecnfs() writes the vector V to the output file *pf
and returns 0 or ERROR in case an error has occurred.
.PP
.TP 4
.B F
is an irreducible monic sparse polynomial in one variable over the rational numbers.
.TP 4
.B V
is a vector over the number field generated by F.
.TP 4
.B VL
is a variable list of length 1.
.TP 4
.B pf
is a pointer to the output file.

.SH SEE ALSO
fputnfels
