/*M
.SH NAME
fputvecnf - file put vector of number field elements (MACRO)

.SH SYNOPSIS
.nf
#include<_matr2.h>

single fputvecnf(F, V, VL ,pf)
pol F;
vec V;
list VL;
FILE *pf;

.SH DESCRIPTION
fputvecnf() writes the vector V to the output file *pf
and returns 0 or ERROR in case an error has occurred.
.PP
.TP 4
.B F
is an irreducible monic dense polynomial in one variable over Z.
.TP 4
.B M
is a vector over the number field generated by F.
.TP 4
.B VL
is a variable list of length 1.
.TP 4
.B pf
is a pointer to the output file.

