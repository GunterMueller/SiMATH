/*M
.SH NAME
vecnfsprod - vector of number field elements, scalar product

.SH SYNOPSIS
.nf
#include<_matr2.h>

nfel vecnfsprod(F, U, V)
pol F;
vec U,V;

.SH DESCRIPTION
vecnfsprod() returns the scalar product U*V.

.PP
.TP 4
.B F
is an irreducible monic dense polynomial in one variable over Z.
.TP 4
.B U,V
are vectors over the number field generated by F.
U and V must have equal length.
M*/

/*H
	Version 1       12.04.1990      Th. Weis
   DATE vecnfsprod    : 910915
H*/

#include <_matr2.h>

nfel vecnfsprod( F, V, W )
pol F;
vec V,W;

{
	nfel s,v,w;

	bind( F, V, W );
	init( s, v, w );

	s = 0;
	while ( V != _0 )
	{
	   v = lfirst( V ), V = lred( V );
	   w = lfirst( W ), W = lred( W );
	   s = nfsum( F, nfprod( F, v, w ), s );
	}
	return( s );
}
