fgetvecnfs - file get vector of number field elements, sparse representation (MACRO)

.SH SYNOPSIS
.nf
#include<_matr2.h>

vec fgetvecnfs(F, VL, pf)
pol F;
list VL;
FILE *pf;

.SH DESCRIPTION
fgetvecnfs() returns the vector of elements of the number field generated by F,
read from the input file *pf or ERROR in case an error has occurred.

.PP
.TP 4
.B F
is an irreducible monic sparse polynomial in one variable over the rational numbers.
.TP 4
.B VL
is a variable list of length 1.
.TP 4
.B pf
is a pointer to the input file.

