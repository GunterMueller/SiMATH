/*M
.SH NAME
vecnfssprod - vector of number field elements, sparse representation, scalar product

.SH SYNOPSIS
.nf
#include<_matr2.h>

nfel vecnfssprod(F, U, V)
pol F;
vec U,V;

.SH DESCRIPTION
vecnfssprod() returns the scalar product U*V.

.PP
.TP 4
.B F
is an irreducible monic sparse polynomial in one variable over the rational numbers.
.TP 4
.B U,V
are vectors over the number field generated by F.
U and V must have equal length.
M*/



/*H
	Version 1       12.04.1990      Th. Weis
   DATE vecnfssprod   : 910915
H*/

#include <_matr2.h>

nfel vecnfssprod( F, V, W )
pol F;
vec V,W;

{
	nfel s,v,w;

	bind( F, V, W );
	init( s, v, w );

	s = 0;
	while ( V != _0 )
	{
	   v = lfirst( V ), V = lred( V );
	   w = lfirst( W ), W = lred( W );
	   s = nfssum( F, nfsprod( F, v, w ), s );
	}
	return( s );
}
